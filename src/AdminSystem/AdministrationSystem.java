    //METODO PARA CARGAR DATOS DE LA DB DE FACTURAS
    public void cargarDatosFacturas() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("# Factura");
            modelo.addColumn("FechaEmision");
            modelo.addColumn("Detalles");
            modelo.addColumn("MetodoPago");
            modelo.addColumn("PrecioBruto");
            modelo.addColumn("Impuestos");
            modelo.addColumn("Descuentos");
            modelo.addColumn("PrecioNeto");

            DBadminsystem cc = new DBadminsystem();
            Connection cn = cc.DBcontrolProductos();

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM facturacion");

            while (rs.next()) {
                String[] fila = new String[8];
                fila[0] = rs.getString(1);
                fila[1] = rs.getString(2);
                fila[2] = rs.getString(3);
                fila[3] = rs.getString(4);
                fila[4] = rs.getString(5);
                fila[5] = rs.getString(6);
                fila[6] = rs.getString(7);
                fila[7] = rs.getString(8);
                modelo.addRow(fila);
            }
            TablaFacturas.setModel(modelo);

        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }
    
