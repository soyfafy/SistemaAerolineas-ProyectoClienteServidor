            String impuestos = txtImpuestos.getText();
            String descuentos_aplicados = txtDescuentosApli.getText();
            String precio_neto = txtPrecioNeto.getText();

            
            ps.setString(1, fecha_emision);
            ps.setString(2, detalles);
            ps.setString(3, metodo_pago);
